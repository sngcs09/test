#include<stdio.h>
void main()
{
int a[30],key,n,i,f=0;
printf("enter the size of array\n");
scanf("%d",&n);
printf("enter the array elements\n");
for(i=0;i<n;i++)
{
scanf("%d",&a[i]);
}
printf("enter an element to search\n");
scanf("%d",&key);
for(i=0;i<n;i++)
{
if(a[i]==key)
{
printf("element found at %d",i);
f=1;
}
}
if(f==0)
{
printf("element not found\n");
}
}
